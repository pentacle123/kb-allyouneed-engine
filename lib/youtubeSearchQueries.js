/**
 * opportunity id로 검색 쿼리 배열 조회
 * @param {string} opportunityId - 예: "P3-3", "FAMILY-COVER-5"
 * @returns {string[]} 쿼리 배열 (없으면 빈 배열)
 */
export function getYoutubeQueries(opportunityId) {
  return YOUTUBE_SEARCH_QUERIES[opportunityId] || [];
}

/**
 * 전체 매핑 통계
 */
export function getQueryStats() {
  const ids = Object.keys(YOUTUBE_SEARCH_QUERIES);
  const total = ids.length;
  const totalQueries = ids.reduce((sum, id) => sum + YOUTUBE_SEARCH_QUERIES[id].length, 0);
  return {
    mappedOpportunities: total,
    totalQueries,
    avgQueriesPerOpportunity: (totalQueries / total).toFixed(1)
  };
}
